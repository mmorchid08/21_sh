#include "libft.h"

char	*ft_strmap2(char *s, char (*f)(char))
{
	int		i;

	i = 0;
	if (s && f)
	{
		while (s[i])
		{
			s[i] = f(s[i]);
			i++;
		}
		return (s);
	}
	return (NULL);
}
